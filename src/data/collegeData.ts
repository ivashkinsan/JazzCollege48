// ===== ИНТЕРФЕЙСЫ =====

export interface Teacher {
  id: string;
  name: string;
  position: string;
  specialty: string;
  image?: string;
  bio?: string;
}

export interface Achievement {
  id: string;
  title: string;
  studentName?: string;
  competition: string;
  date: string;
  place: string;
  category: string;
  image?: string;
}

export interface Graduate {
  id: string;
  name: string;
  graduationYear: number;
  city: string;
  position: string;
  workplace?: string;
  image?: string;
  bio?: string;
}

export interface Concert {
  id: string;
  title: string;
  date: string;
  time?: string;
  venue: string;
  description: string;
  image?: string;
  isFree?: boolean;
}

export interface Ensemble {
  id: string;
  name: string;
  type: string;
  description: string;
  members?: string[];
}

export interface NewsItem {
  id: string;
  title: string;
  date: string;
  description: string;
  image?: string;
}

export interface Specialty {
  code: string;
  name: string;
  profiles: string[];
  description: string;
  qualification?: string;
  studyDuration?: string;
  studyForm?: string;
}

export interface Instrument {
  id: string;
  name: string;
  category: 'клавишные' | 'духовые' | 'ударные' | 'струнные';
  description: string;
}

export interface EstradaDepartment {
  name: string;
  established: number;
  description: string;
  headName: string;
  specialties: Specialty[];
  features: string[];
  instruments: Instrument[];
}

export interface NavigationItem {
  id: string;
  label: string;
  href: string;
}

export interface CollegeInfo {
  name: string;
  shortName: string;
  address: string;
  phone: string;
  email: string;
  website: string;
}

